public class JavaBoolean {
public static void main(String[] args){
/* 
Very often in programming, you will need a data type that 
can only have one of two values, like:

YES / NO
ON / OFF
TRUE / FALSE
For this, Java has a boolean data type, which can only take 
the values true or false:
*/

boolean isTodayChristmas = false;
boolean isTodayBoxingDay = true;

System.out.println("Boolean : " + isTodayChristmas);
System.out.println("Boolean : " + isTodayBoxingDay);
}
}
